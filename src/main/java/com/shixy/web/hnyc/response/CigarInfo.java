package com.shixy.web.hnyc.response;

public class CigarInfo {
	private String columId;
	private String content;
	private String maps_show;
	private String pads_show;
	private String pads_src;
	private String pindex;
	private String pshow;
	private String purl;

	public String getColumId() {
		return columId;
	}

	public void setColumId(String columId) {
		this.columId = columId;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getMaps_show() {
		return maps_show;
	}

	public void setMaps_show(String maps_show) {
		this.maps_show = maps_show;
	}

	public String getPads_show() {
		return pads_show;
	}

	public void setPads_show(String pads_show) {
		this.pads_show = pads_show;
	}

	public String getPads_src() {
		return pads_src;
	}

	public void setPads_src(String pads_src) {
		this.pads_src = pads_src;
	}

	public String getPindex() {
		return pindex;
	}

	public void setPindex(String pindex) {
		this.pindex = pindex;
	}

	public String getPshow() {
		return pshow;
	}

	public void setPshow(String pshow) {
		this.pshow = pshow;
	}

	public String getPurl() {
		return purl;
	}

	public void setPurl(String purl) {
		this.purl = purl;
	}

}
